def find_script_ranges(characters):
    ranges = []
    start = ord(characters[0])
    end = ord(characters[0])
    for char in characters[1:]:
        code_point = ord(char)
        if code_point == end + 1:
            end = code_point
        else:
            ranges.append((start, end))
            start = end = code_point
    ranges.append((start, end))
    return ranges


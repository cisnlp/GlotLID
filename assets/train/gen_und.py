import random
def convert_ranges_to_numbers(ranges):
    numbers = []
    for start, end in ranges:
        numbers.extend(range(start, end + 1))
    return numbers


def generate_sentences(script, num_sentences=5, min_size_sent = 1, max_size_sent = 100, min_num_space = 0, max_num_space = 10):
    sentences = []
    ranges = SCRIPT_RANGES.get(script)

    numbers = convert_ranges_to_numbers(ranges)
    
    for _ in range(num_sentences):
        
        sentence_length = random.randint(min_size_sent, max_size_sent)
        sentence = ''
        
        # Randomly select characters from the range
        random_chars = [chr(random.choice(numbers)) for _ in range(sentence_length)]

        # Select number of spaces to insert
        num_spaces = random.randint(min_num_space, max_num_space)
        
        num_spaces = min(num_spaces, len(random_chars))
        
        # Randomly select positions to insert spaces
        space_positions = random.sample(range(len(random_chars)), num_spaces)
        
        # Insert spaces into random_chars at selected positions
        for pos in sorted(space_positions, reverse=True):
            random_chars.insert(pos, ' ')

        # Join characters
        sentence += ''.join(random_chars)

        sentences.append(sentence)

    return sentences



def save_list_to_file(lst, filename):
    with open(filename, 'w') as file:
        for item in lst:
            file.write(str(item) + '\n')


